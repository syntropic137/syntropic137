# Custom YAML output
# =============================================================================


class _ComposeDumper(yaml.SafeDumper):
    """YAML dumper tuned for Docker Compose files."""


def _str_representer(dumper: _ComposeDumper, data: str) -> yaml.ScalarNode:
    """Represent strings with minimal quoting for Docker Compose conventions."""
    if not data:
        return dumper.represent_scalar("tag:yaml.org,2002:str", data, style='"')
    if "\n" in data:
        return dumper.represent_scalar("tag:yaml.org,2002:str", data, style="|")
    # Force quotes for values YAML would misinterpret
    if data.lower() in {"true", "false", "yes", "no", "on", "off", "null", "~"}:
        return dumper.represent_scalar("tag:yaml.org,2002:str", data, style='"')
    # Quote pure numbers (Docker Compose env values like "8080" need quotes)
    try:
        float(data.replace(":", "").replace(",", ""))
        if data.replace(".", "").replace("-", "").isdigit() or data in ("0.0.0.0",):
            return dumper.represent_scalar("tag:yaml.org,2002:str", data, style='"')
    except ValueError:
        pass
    # Default: let PyYAML choose (it will quote if needed for YAML safety)
    return dumper.represent_scalar("tag:yaml.org,2002:str", data)


_ComposeDumper.add_representer(str, _str_representer)


def _postprocess(text: str) -> str:
    """Clean up PyYAML output to match Docker Compose conventions."""
    # PyYAML single-quotes strings with ${} — remove those quotes.
    # Pattern: key: '...${...}...' → key: ...${...}...
    text = re.sub(r"(?<=: )'([^']*\$\{[^']*}[^']*)'", r"\1", text)

    # Also handle double-quoted ${} patterns
    text = re.sub(r'(?<=: )"([^"]*\$\{[^"]*}[^"]*)"', r"\1", text)

    # Remove quotes from simple path values: './foo', '../foo'
    text = re.sub(r"(?<=: )'(\./[^']*)'", r"\1", text)

    return text


def render(data: dict) -> str:
    """Render the compose dict to a YAML string with header."""
    stream = StringIO()
    yaml.dump(
        data,
        stream,
        Dumper=_ComposeDumper,
        default_flow_style=False,
        sort_keys=False,
        allow_unicode=True,
        width=120,
    )
    yaml_text = _postprocess(stream.getvalue())
    return FILE_HEADER + yaml_text


# =============================================================================
# Normalization for --check comparison
# =============================================================================


def _normalize(data: object) -> object:
    """Normalize a parsed YAML structure for comparison.

    Handles type coercion differences (int vs str in env values, etc.).
    """
    if isinstance(data, dict):
        return {str(k): _normalize(v) for k, v in data.items()}
    if isinstance(data, list):
        return [_normalize(item) for item in data]
    if data is None:
        return None
    return data


# =============================================================================
# Main
# =============================================================================


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate published Docker Compose for self-hosters",
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help="Check committed file matches generated (CI mode)",
    )
    args = parser.parse_args()

    data = generate()

    if args.check:
        if not OUTPUT_FILE.exists():
            print(
                f"ERROR: {OUTPUT_FILE.relative_to(PROJECT_ROOT)} does not exist",
                file=sys.stderr,
            )
            sys.exit(1)
        committed_data = yaml.safe_load(OUTPUT_FILE.read_text())
        if _normalize(committed_data) == _normalize(data):
            print(f"OK: {OUTPUT_FILE.name} is up to date")
            sys.exit(0)

        # Show what diverged
        print(
            f"ERROR: {OUTPUT_FILE.name} is out of sync with source compose files",
            file=sys.stderr,
        )
        _show_diff(committed_data, data)
        print("\nRun: just gen-compose", file=sys.stderr)
        sys.exit(1)
    else:
        output = render(data)
        OUTPUT_FILE.write_text(output)
        print(f"Generated: {OUTPUT_FILE.relative_to(PROJECT_ROOT)}")


def _show_diff(committed: object, generated: object, path: str = "") -> None:
    """Print structural differences between committed and generated."""
    if type(committed) is not type(generated):
        print(f"  {path}: type mismatch ({type(committed).__name__} vs {type(generated).__name__})")
        return
    if isinstance(committed, dict) and isinstance(generated, dict):
        all_keys = set(committed) | set(generated)
        for key in sorted(all_keys):
            child_path = f"{path}.{key}" if path else key
            if key not in committed:
                print(f"  + {child_path} (missing in committed)")
            elif key not in generated:
                print(f"  - {child_path} (extra in committed)")
            elif _normalize(committed[key]) != _normalize(generated[key]):
                _show_diff(committed[key], generated[key], child_path)
    elif isinstance(committed, list):
        if _normalize(committed) != _normalize(generated):
            print(f"  {path}: list differs")
    elif str(committed) != str(generated):
        print(f"  {path}: {committed!r} → {generated!r}")


if __name__ == "__main__":
    main()
